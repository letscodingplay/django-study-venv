import turtle

t = turtle.Turtle()


t.shape("turtle")

t.up()
t.goto(-200, 100)
t.down()
t.circle(100)

t.up()
t.goto(-10, 100)
t.down()
t.circle(100)

t.up()
t.goto(180, 100)
t.down()
t.circle(100)

t.up()
t.goto(-100, -30)
t.down()
t.circle(100)

t.up()
t.goto(90, -30)
t.down()
t.circle(100)

turtle.mainloop()

